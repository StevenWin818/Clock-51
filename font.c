// 获取字符索引
unsigned char GetCharIndex(char c) {
    if (c >= '0' && c <= '9')
        return (unsigned char)(c - '0');
    switch (c) {
        case ':': return 10;
        case '-': return 11;
        case '/': return 12;
        case '.': return 11; /* 对于24x32字体，点用索引11 */
        default:  return 13; /* 空格 */
    }
}
